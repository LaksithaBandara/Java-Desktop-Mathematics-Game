    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        
        String link1 = "";
        BufferedImage image = null;
        
        int answered = Integer.parseInt(lblquestionAnswered.getText());
        int nAnswered = 4-answered;
        
        float answeredPer=(float) ((answered/4.0)*100);
        float nAnsweredPer=100-answeredPer;
        
        
        try {
            //url connect API
                link1 = "https://quickchart.io/chart?bkg=white&c={ type: 'pie', data: { datasets: [ { data: [\'" + answeredPer + "\',\'" + nAnsweredPer + "\',], backgroundColor: [ 'rgb(0, 255, 221)', 'rgb(255, 128, 128)', ], label: 'Dataset 1', }, ], labels: ['Correct Percentage', 'Incorrect Percentage'], },}";

                link1 = link1.replace(" ", "");

                URL url1 = new URL(link1);

                HttpURLConnection urlcon = (HttpURLConnection) url1.openConnection();
             //requst propperties
                urlcon.setRequestProperty("User-Agent",
                        "Mozilla/5.0 (Windows NT 6.1; WOW64) AppleWebKit/537.11 (KHTML, like Gecko) Chrome/23.0.1271.95 Safari/537.11");

                image = ImageIO.read(urlcon.getInputStream());

                JFrame frame = new JFrame();

                frame.setForeground(Color.WHITE);

                frame.setResizable(false);
                frame.setBackground(Color.WHITE);
                frame.setSize(1000, 850);
                JLabel label = new JLabel(new ImageIcon(image));
                frame.getContentPane().add(label);
                frame.setVisible(true);
                frame.setLocationRelativeTo(null);
            } catch (MalformedURLException e1) {
                // TODO Auto-generated catch block
                e1.printStackTrace();

            } catch (IOException e1) {
                JOptionPane.showMessageDialog(null, "Not Internet Connection", "ALERT", JOptionPane.WARNING_MESSAGE);
                // TODO Auto-generated catch block
                e1.printStackTrace();
            }
    }//GEN-LAST:event_jButton3ActionPerformed

